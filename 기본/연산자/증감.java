package 기본.연산자;

public class 증감 {
    
    public static void main(String[] args) {
        
        int a = 10;
        System.out.println("현재의 a는" + a + "입니다.");
        a++;
        System.out.println("현재의 a는" + a + "입니다.");
        System.out.println("현재의 a는" + ++a + "입니다.");
        System.out.println("현재의 a는" + a++ + "입니다.");
        System.out.println("현재의 a는" + a + "입니다.");


    }
}
